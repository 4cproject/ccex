    <h3 class="media-heading" style="padding-top: 20px;">
        <span class="label label-success">Step 6</span><br><br>
            Compare costs
    </h3>
    <p>The tool now lets you see how your expenses compare with those of other organisations.</p>
    <p>In the <b>Global comparison</b> section you will see how you compare against all other organisations. This is based on an average cost data submitted by all users (unless you apply one of the filters). </p>
    <p>Select the <b>Peer comparison</b> tab for a comparison with individual organisations similar to yours. You can also request a contact with your preferred organisation to share experience and discuss ways to better manage costs. </p>
