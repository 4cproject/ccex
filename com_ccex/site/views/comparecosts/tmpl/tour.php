    <h3 class="media-heading" style="padding-top: 20px;">
        <span class="label label-success">Step 5</span><br><br>
            Completing a cost data set
    </h3>
    <p>You can add several cost units to a cost data set. For example, the total cost of a digitisation project can include detailed costs of hardware, software, scanning, quality assurance, etc.</p>
    <p>Once you are finished adding cost units, switch to <b>Final</b>. Don't worry, you can return to <b>Draft</b> mode anytime you want to update information.</p>
    <p>Select <b>Analyse and compare costs</b>.</p>
