    <h3 class="media-heading" style="padding-top: 20px;">
        <span class="label label-success">Step 4</span><br><br>
            Add cost unit
    </h3>
    <p>Tell us how much money you spend on various services and map the overall cost to the categories provided. First, allocate the amount to curation activities. Next, map it to staff and purchases.</p>

