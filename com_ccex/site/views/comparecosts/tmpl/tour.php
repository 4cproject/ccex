    <h3 class="media-heading" style="padding-top: 20px;">
        <span class="label label-success">Step 1</span><br><br>
            Create an account (Sign up)
    </h3>
    <p>Select <b>Sign up</b>. Fill in the form and press <b>Register</b>. After a few minutes you will receive an email message asking you to confirm registration.</p>
    <div class="alert alert-info" role="alert"><b>Can't find email?</b> Please check your junk mail folder in case the confirmation email got delivered there instead of your inbox.</div>
  </div>
</div>
<div class="row">
  <div class="col-md-6">
    <img class="img-responsive" alt="Add organisation" src="/templates/ccextemplate/images/screenshots/add_organization.png">
  </div>
  <div class="col-md-6">

    <h3 class="media-heading" style="padding-top: 20px;">
        <span class="label label-success">Step 2</span><br><br>
            Add organisation
    </h3>
    <p>Enter your organisation details. This will enable the tool to make cost comparisons between organisations and to suggest institutions similar to yours (peer organisations) for more targeted comparisons.</p>

    <p>In the <b>Information sharing</b> section tick boxes to set your preferences (these can be changed later).</p>
  </div>
</div>
<div class="row">
  <div class="col-md-6">
